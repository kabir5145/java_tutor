package com.company;

public class cwh_22_ch4_do_while {
    public static void main(String[] args) {
        int b = 0;
        do {
            System.out.println(b);
            b++;
        }while (b<5);

        int c =1;
        do{
            System.out.println(c);
            c++;
        }while (c<=45);
    }
}
